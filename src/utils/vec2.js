/**
 * @param {Vec2} a
 * @param {Vec2} b
 * @returns {Vec2}
 */
export function add(a, b) {
	return [a[0] + b[0], a[1] + b[1]];
}
/**
 * @param {Vec2} a
 * @param {Vec2} b
 * @returns {Vec2}
 */
export function sub(a, b) {
	return [a[0] - b[0], a[1] - b[1]];
}

